    : IComparable<Guid>
    , IComparable<Uuid7>
    , IEquatable<Uuid7>
    , IEquatable<Guid>
    , IFormattable
#if NET6_0_OR_GREATER
    , ISpanFormattable
#endif
#if NET7_0_OR_GREATER
    , ISpanParsable<Uuid7>
#endif
