        if (id25Text == null) { throw new ArgumentNullException(nameof(id25Text), "Text cannot be null."); }
        if (TryParseAsId25(id25Text.ToCharArray(), out var result)) {
            return result;
        } else {
            throw new FormatException("Unrecognized UUID format.");
        }
    }


